    class Mat {
    // Metode pertambahan
    public double tambah(double a, double b, double c) {
        return a + b + c;
    }
    public int tambah(int a, int b, int c) {
        return  a %+ b + c; 
    }
     public int tambah(int a, int b) {
        return a + b;
    }
    public double tambah(double a, double b) {
        return a + b ;
    }
    

    // Metode pengurangan
    public double kurang(double a, double b, double c) {
        return a - b - c;
    }
    public int kurang(int a, int b, int c) {
        return  a - b - c;
    }
    public int kurang(int a, int b) {
        return a - b;
    }
    public double kurang(double a, double b) {
        return a - b;
    }

    // Metode perkalian
    public double kali(double a, double b, double c) {
        return a * b * c;
    }
    public int kali(int a, int b, int c) {
        return a * b * c;
    }
    public int kali(int a, int b) {
        return a * b;
    }
    public double kali(double a, double b) {
        return a * b;
    
    }
    public double bagi(double a, double b, double c) {
        return a / b / c;
    }
    public int bagi(int a, int b, int c) {
        return a / b / c;
    }
    public int bagi(int a, int b) {
        return a / b;
    }
    public double bagi(double a, double b) {
        return a / b;
    }
    
}

